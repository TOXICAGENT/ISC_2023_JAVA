import java.util.Scanner;
public class MathFunctions
{
    public static void main(String args[])
    {
        System.out.println("Math.rint(17.99): "+Math.rint(17.99)); // round up
        System.out.println("e raced to 4 "+Math.exp(4));            
        System.out.println("ceil of 17.3 "+Math.ceil(17.3));       // round up
        System.out.println("floor of 17.3: "+Math.floor(17.3));    // round down
    }
}
